// Arthmetic operator
#include <stdio.h>

int main()
{
    int num1;
    int num2;
    printf("Enter the first number");

    scanf("%d",&num1);

    printf("Enter the second number");

    scanf("%d",&num2);

    printf("the sum is %d\n",num1+num2);
    printf("the product is %d\n",num1*num2);
    printf("the difference is %d\n",num1-num2);
    printf("the ratio is %d\n",num1/num2);
    return 0;

}
